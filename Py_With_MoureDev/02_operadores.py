## Operadores Aritmeticos##

"""
print(1//2) => division con resultado entero
print(1%2) => el resto => para los multiplos
print(1**2) =>  potencia
"""

num_1, num_2 = 12, 2

print(num_1+num_2)
print(num_1-num_2)
print(num_1*num_2)
print(num_1/num_2)
print(num_1//num_2)
print(num_1 % num_2)
print(num_1**num_2)
print("Repetir "*2)


